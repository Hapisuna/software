int ran(int a)
{
	int b,c,d,e,f,g,h,j,k,m;
	b = a * a;
	c = b / 10000000;	//一桁目
	d = (b - (c * 10000000)) / 1000000;		//二桁目
	e = (b - (c * 10000000) - (d * 1000000)) / 100000;		//三桁目
	f = (b - (c * 10000000) - (d * 1000000) - (e * 100000)) / 10000;	//四桁目
	g = (b - (c * 10000000) - (d * 1000000) - (e * 100000) - (f * 10000)) / 1000;	//五桁目
	h = (b - (c * 10000000) - (d * 1000000) - (e * 100000) - (f * 10000) - (g * 1000)) / 100;	//六桁目
	j = (b - (c * 10000000) - (d * 1000000) - (e * 100000) - (f * 10000) - (g * 1000) - (h * 100)) / 10;	//七桁目
	k = (b - (c * 10000000) - (d * 1000000) - (e * 100000) - (f * 10000) - (g * 1000) - (h * 100) - (j * 10)) / 1;		//八桁目

	m = e * 1000 + f * 100 + g *10 + h;
	return m;	
}